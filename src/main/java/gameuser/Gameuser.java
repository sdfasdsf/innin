package gameuser;

public class Gameuser {
	private String gameuserID;
	private String gameuserPW; 
	private String gameuserNAME;
	private String gameuserGENDER; 
	private String gameuserEMAIL;
	public String getGameuserID() {
		return gameuserID;
	}
	public void setGameuserID(String gameuserID) {
		this.gameuserID = gameuserID;
	}
	public String getGameuserPW() {
		return gameuserPW;
	}
	public void setGameuserPW(String gameuserPW) {
		this.gameuserPW = gameuserPW;
	}
	public String getGameuserNAME() {
		return gameuserNAME;
	}
	public void setGameuserNAME(String gameuserNAME) {
		this.gameuserNAME = gameuserNAME;
	}
	public String getGameuserGENDER() {
		return gameuserGENDER;
	}
	public void setGameuserGENDER(String gameuserGENDER) {
		this.gameuserGENDER = gameuserGENDER;
	}
	public String getGameuserEMAIL() {
		return gameuserEMAIL;
	}
	public void setGameuserEMAIL(String gameuserEMAIL) {
		this.gameuserEMAIL = gameuserEMAIL;
	}
	
	
}

